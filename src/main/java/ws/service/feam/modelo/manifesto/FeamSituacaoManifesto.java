package ws.service.feam.modelo.manifesto;

public class FeamSituacaoManifesto {
    private int    simCodigo;
    private String simDescricao;
    private int    simOrdem;

    public int getSimCodigo() {
        return simCodigo;
    }

    public void setSimCodigo(int simCodigo) {
        this.simCodigo = simCodigo;
    }

    public String getSimDescricao() {
        return simDescricao;
    }

    public void setSimDescricao(String simDescricao) {
        this.simDescricao = simDescricao;
    }

    public int getSimOrdem() {
        return simOrdem;
    }

    public void setSimOrdem(int simOrdem) {
        this.simOrdem = simOrdem;
    }


}
