package ws.service.feam.modelo.manifesto;

public class FeamUnidadeResiduo {
    private Integer uniCodigo;
    private String uniDescricao;
    private String uniSigla;

    public Integer getUniCodigo() {
        return uniCodigo;
    }

    public void setUniCodigo(Integer uniCodigo) {
        this.uniCodigo = uniCodigo;
    }

    public String getUniDescricao() {
        return uniDescricao;
    }

    public void setUniDescricao(String uniDescricao) {
        this.uniDescricao = uniDescricao;
    }

    public String getUniSigla() {
        return uniSigla;
    }

    public void setUniSigla(String uniSigla) {
        this.uniSigla = uniSigla;
    }


}
